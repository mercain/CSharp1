using System;


class FloatOrDouble
{
    static void Main()
    {
        double numOne = 34.567839023;
        float numTwo = 12.345F;
        float numThree = 8923.1234857F;
        float numFour = 3456.091F;

        Console.WriteLine("{0}, {1}, {2}, {3}", numOne, numTwo, numThree, numFour);
    }
}
